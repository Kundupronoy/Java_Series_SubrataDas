public class typeCasting {

    public static void main(String[] args) {
        
        int a = 65;
        double b =  a ;  // TypeCasting { we are actually convert one data type to another data Type} // widening Casting
        
        char c = (char) a ; // narrowing casting

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
    
}
